package binance;

public class acao {

	private Integer preço;
	
	private String Nome;
	
	private Integer dividendo;

	public Integer getPreço() {
		return preço;
	}

	public void setPreço(Integer preço) {
		this.preço = preço;
	}
	
	
	public String getNome() {
		return Nome;
	}

	public void setNome(String nome) {
		Nome = nome;
	}


	public Integer getDividendo() {
		return dividendo;
	}

	public void setDividendo(Integer dividendo) {
		this.dividendo = dividendo;
	}


	
	
}
